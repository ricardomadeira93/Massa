import { Language, LocalizedText } from '@/types';

export const defaultLanguage: Language = 'pt';
export const supportedLanguages: Language[] = ['pt', 'en', 'es'];
export const languageStorageKey = 'massa-language';

export const languageMeta: Record<Language, { shortLabel: string; label: string; htmlLang: string }> = {
  pt: { shortLabel: 'PT', label: 'Português', htmlLang: 'pt-BR' },
  en: { shortLabel: 'EN', label: 'English', htmlLang: 'en' },
  es: { shortLabel: 'ES', label: 'Español', htmlLang: 'es' },
};

export function translateText(value: LocalizedText | string, language: Language): string {
  if (typeof value === 'string') {
    return value;
  }

  return value[language] ?? value[defaultLanguage];
}

